/*
Да се напише програма која ќе чита два цели броеви (x, y) од тастура и на компјутерскиот екран ќе го испечати резултатот (z) од следниот израз
z = x++ + --y + (x<y)
Каква вредност ќе има z за x = 1, y = 2?
*/

#include <stdio.h>

int main()
{
    int x,y,z;
    scanf("%d %d",&x,&y);
    z = x++ + --y + (x<y);
    printf("%d",z);
    return 0;
}
