/*
Нека е дадено:
    r = (x<y || y<z++)
    
Каква вредност ќе има r за x=1, y=2, z=3?
Каква вредност ќе има z?

===========================================

Нека е дадено:

  r = (x>y && y<z++)
  
Каква вредност ќе има r за x=1, y=2, z=3?
Каква вредност ќе има z?
*/

#include <stdio.h>

int main()
{
    int x,y,z;
    x = 1;
    y = 2;
    z = 3;

    int r;
    r = (x<y || y<z++);
    printf("r = %d, z = %d\n",r,z);

    r = (x>y && y<z++);
    printf("r = %d, z = %d",r,z);
    return 0;
}
