/*
Да се напише програма за пресметување на сумата на сите непарни двоцифрени броеви. Програмата ја печати сумата на екран во следниот формат:
11 + 13 + 15 + 17 + ... + 97 + 99 = 2475
*/

#include <stdio.h>

int main() {
    int i;
    i=11;
    int sum=0;
    while(i<=99)
    {
        printf(" + %d",i);
        sum = sum + i;
        i=i+2;
    }
    printf(" = %d",sum);
    return 0;
}
