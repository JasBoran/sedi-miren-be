/*
Да се напише програма која пресметува просечна оцена во семестар. Програмата чита 5 цели броја. Треба да се испечати еден реален број на две децимали, 
просек на прочитаните броеви.

For Example:
Input: 6 6 6 6 7
Output: 6.20
*/

#include <stdio.h>

int main() {
    float a,b,c,d,e;
    scanf("%f %f %f %f %f", &a,&b,&c,&d,&e);
    printf("%.2f",(a+b+c+d+e)/5);
    return 0;
}
